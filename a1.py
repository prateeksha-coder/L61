num1 = 10 #1010
num2 = 4  #0100
print("num1 & num2 are:", num1 & num2)
print("num1 | num2 are:", num1 | num2)
print("num1 ^ num2 are:", num1 ^ num2)
#Using right shift operator
print("num1 >> 1 are:", num1 >>1)
print("num2 >> 1 are:", num2 >>1)
#Using left shift operator
print("num1 << 1 are:", num1 <<1)
print("num2 << 1 are:", num2 <<1)